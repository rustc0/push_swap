#include "libft.h"

long	ft_atol(const char *str)
{
	int		i;
	int		n;
	long	ret;

	i = 0;
	n = 1;
	ret = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			n = -1;
		else
			n = 1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		ret = ret * 10 + (str[i] - '0');
		if (ret * n > INT_MAX || ret * n < INT_MIN)
			return (LONG_MAX);
		i++;
	}
	return (ret * n);
}
